# Using the 'in' operator


# The 'in' operator checks if a value exists in a sequence (like a string or a list).
# It returns True if the value is found, otherwise it returns False.


# Example with a string

name = "arya"
if "a" in name:  # Check if 'a' is in the string 'name'
    print("correct")  # Output: correct
else:
    print("wrong")
    
    

# Example with a list

list_in = ['a', 'b', 'e', 't']
if "a" in list_in:  # Check if 'a' is in the list 'list_in'
    print("correct")  # Output: correct
else:
    print("wrong")