# Using the len() function


# Define a list

list_1 = [1, 2, 3, 4, 5, 6]



# Get the length of the list

length = len(list_1)  # Length is 6



# Print the length

print("Length of list_1:", length)  # Output: Length of list_1: 6



# Accessing the last element using length

print(list_1[length - 1])  # Output: 6

# This is equivalent to:

print(list_1[-1])          # Output: 6



# Indexing explanation:
# List indices:
#      0   1   2   3   4   5 

# Negative indices:
#     -6  -5  -4  -3  -2  -1 