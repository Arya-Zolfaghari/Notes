# For Loop




# For List

ab = [1, 2, 3, 4]
for i in ab:
    print(f"The number is {i}") 
    # Print each number in the list




# For String

ch = "Hello Arya"
for c in ch:
    print(c)  
    # Print each character on a new line




# For Range

for i in range(3):
    print(1) 
    # Print '1' three times